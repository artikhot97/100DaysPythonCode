if direction == "left":
  print("You enter the dark forest. You hear a rustling in the bushes. Do you investigate?")
  investigate = input("Type 'yes' or 'no'.\n").lower()
  if investigate == "yes":
    print("You find a small creature. It looks like a bear. Do you try to pet it?")
    pet = input("Type 'yes' or 'no'.\n").lower()
    if pet == "yes":
      print("The bear is very friendly and gives you a treasure map. You win!")
    else:
      print("The bear attacks you and you die. Game over.") 
  else:
    print("You continue on your journey. You come across a river. Do you swim across or wait for a boat?")
    swim = input("Type 'swim' or 'wait'.\n").lower()
    if swim == "swim":
      print("You swim across the river and find a treasure chest. You win!")
    else:
      print("A boat arrives and takes you to the other side of the river. You find a treasure chest. You win!")


